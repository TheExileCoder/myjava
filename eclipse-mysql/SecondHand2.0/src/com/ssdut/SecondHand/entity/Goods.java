package com.ssdut.SecondHand.entity;

public class Goods {
	private int goodsId;
	private String goodsName;
	private int goodsNum;
	private float goodsPrice;
	private int goodsDay;
	private String goodscommon;

	public int getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsname) {
		this.goodsName = goodsname;
	}
	
	public int getGoodsNum() {
		return goodsNum;
	}
	
	public void setGoodsNum(int goodsNum) {
		this.goodsNum = goodsNum;
	}
	
	public float getGoodsPrice() {
		return goodsPrice;
	}
	
	public void setGoodsPrice(float goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public int getGoodsDay() {
		return goodsDay;
	}

	public void setGoodsDay(int goodsDay) {
		this.goodsDay = goodsDay;
	}

	public String getGoodscommon() {
		return goodscommon;
	}

	public void setGoodscommon(String goodscommon) {
		this.goodscommon = goodscommon;
	}

}
